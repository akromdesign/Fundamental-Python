# Operator, Operand, dan Aritmatika

# Contoh penggunaan operator dan operand
x = 5  # Operand 1
y = 3  # Operand 2
z = x + y  # Operator +
print(z)  # Output: 8

# Contoh penggunaan operator aritmatika
a = 10
b = 4
c = a - b  # Operator pengurangan (-)
print(c)  # Output: 6

d = a * b  # Operator perkalian (*)
print(d)  # Output: 40

e = a / b  # Operator pembagian (/)
print(e)  # Output: 2.5

f = a % b  # Operator modulus (%)
print(f)  # Output: 2

g = a ** b  # Operator pangkat (**)
print(g)  # Output: 10000

# Contoh penggunaan operator aritmatika dengan ekspresi kompleks
h = (x + y) * (a - b)  # Ekspresi kompleks
print(h)  # Output: 56

# Contoh penggunaan operator penugasan
i = 7
i += 3  # Operator penugasan penjumlahan (i = i + 3)
print(i)  # Output: 10

j = 12
j *= 2  # Operator penugasan perkalian (j = j * 2)
print(j)  # Output: 24

k = 15
k /= 5  # Operator penugasan pembagian (k = k / 5)
print(k)  # Output: 3.0

# Contoh penggunaan operator perbandingan
p = 5
q = 7
r = p > q  # Operator perbandingan (>), hasilnya adalah False
print(r)  # Output: False

s = p == q  # Operator perbandingan (==), hasilnya adalah False
print(s)  # Output: False

t = p != q  # Operator perbandingan (!=), hasilnya adalah True
print(t)  # Output: True

# Contoh penggunaan operator logika
u = True
v = False
w = u and v  # Operator logika AND, hasilnya adalah False
print(w)  # Output: False

x = u or v  # Operator logika OR, hasilnya adalah True
print(x)  # Output: True

y = not u  # Operator logika NOT, hasilnya adalah False
print(y)  # Output: False
